    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PacientesDAO P = new PacientesDAO();
        List<Pacientes> Consul = P.pa();

        DefaultListModel dlm = new DefaultListModel();

        for(Pacientes p : Consul){
            dlm.addElement(p);
        }
        jList.setModel(dlm);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListMouseClicked
        if(evt.getClickCount() > 1){
            Object objeto = this.jList.getSelectedValue();
            Pacientes selecionar = (Pacientes) objeto;
            
            I_Pacientes pa = new I_Pacientes(selecionar);
            pa.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            pa.setVisible(true);
        }
    }//GEN-LAST:event_jListMouseClicked

