    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Ficha_De_AnamneseDAO f_d_a = new Ficha_De_AnamneseDAO();
        List<Ficha_de_Anamnese> Consul = f_d_a.fa();

        DefaultListModel dlm = new DefaultListModel();

        for(Ficha_de_Anamnese FA : Consul){
            dlm.addElement(FA);
        }
        jList.setModel(dlm);
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListMouseClicked
        if(evt.getClickCount() > 1){
            Object objeto = this.jList.getSelectedValue();
            Ficha_de_Anamnese selecionar = (Ficha_de_Anamnese) objeto;
            
            I_Ficha_de_Anamnese fa = new I_Ficha_de_Anamnese(selecionar);
            fa.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            fa.setVisible(true);
        }
    }//GEN-LAST:event_jListMouseClicked

